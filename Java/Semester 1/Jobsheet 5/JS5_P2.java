public class JS5_P2 {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int x, y;
        String color;
        System.out.print("Input X : ");
        x = scan.nextInt();
        System.out.print("Input Y : ");
        y = scan.nextInt();

        if (x > 0) {
            if (y > 0);
                color = "red";
        } else
            color = "blue";
        System.out.println(color);
    }
}